#include "libft.h"

int	ft_strcchr(char *s1, char *s2, char c)
{
	int	i;

	i = 0;
	while (s1[i] != '\0' && s1[i] != c)
	{
		if (s1[i] != s2[i])
			return (s1[i] - s2[i]);
		i++;
	}
	return (0);
}